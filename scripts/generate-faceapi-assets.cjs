/**
 * generate-faceapi-assets.cjs
 *
 * Embeds face-api.js (dist bundle) + the two model weight files the Trinity
 * session needs (TinyFaceDetector + FaceExpressionNet) into a generated TS
 * module, so the FaceTrackerBridge WebView has ZERO runtime network
 * dependencies (ToDo-stack P1: the old code loaded script + models from
 * vladmandic.github.io at runtime — a conference-WiFi time bomb).
 *
 * Source of truth: the `@vladmandic/face-api` npm package. The mobile repo
 * doesn't depend on it directly; we read it from the web repo's node_modules
 * (the mobile repo lives inside the web repo folder). Override with
 * FACE_API_DIR=/path/to/@vladmandic/face-api if that layout ever changes.
 *
 * Run:  npm run generate:faceapi   (from mobile/)
 * Output: src/generated/faceApiAssets.ts  (committed — builds must not need
 * this script to run)
 */

const fs = require('fs');
const path = require('path');

const pkgDir =
  process.env.FACE_API_DIR ||
  path.join(__dirname, '..', '..', 'node_modules', '@vladmandic', 'face-api');

if (!fs.existsSync(pkgDir)) {
  console.error(`face-api package not found at ${pkgDir} — run npm install in the web repo or set FACE_API_DIR.`);
  process.exit(1);
}

const version = JSON.parse(fs.readFileSync(path.join(pkgDir, 'package.json'), 'utf8')).version;

// --- script -----------------------------------------------------------------
// The iife browser bundle (includes tfjs). `</script` must not appear verbatim
// inside an inline <script> block; `<\/script` is byte-equivalent JS wherever
// it can legally occur (string literal or regex), so the blanket replace is safe.
const script = fs
  .readFileSync(path.join(pkgDir, 'dist', 'face-api.js'), 'utf8')
  .replace(/<\/script/g, '<\\/script');

// --- models -----------------------------------------------------------------
function readModel(name) {
  const manifest = JSON.parse(
    fs.readFileSync(path.join(pkgDir, 'model', `${name}-weights_manifest.json`), 'utf8'),
  );
  // decodeWeights() consumes ONE buffer against ONE spec list — assert the
  // single-group/single-shard layout these models ship with, so a future
  // upgrade to a sharded model fails here instead of silently at runtime.
  if (manifest.length !== 1 || manifest[0].paths.length !== 1) {
    throw new Error(`${name}: expected single-group/single-shard manifest, got ${JSON.stringify(manifest.map((g) => g.paths))}`);
  }
  const bin = fs.readFileSync(path.join(pkgDir, 'model', manifest[0].paths[0].replace(/^\.\//, '')));
  return { weights: manifest[0].weights, b64: bin.toString('base64') };
}

const tiny = readModel('tiny_face_detector_model');
const expression = readModel('face_expression_model');

// --- emit -------------------------------------------------------------------
const out = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-faceapi-assets.cjs — DO NOT EDIT.
// Source: @vladmandic/face-api@${version} (dist/face-api.js + model weights).
// Regenerate after upgrading the package: npm run generate:faceapi

/** face-api.js browser bundle (tfjs included), inlined into the WebView HTML. */
export const FACE_API_SOURCE: string = ${JSON.stringify(script)};

/** tfjs WeightsManifestEntry[] for TinyFaceDetector. */
export const TINY_FACE_DETECTOR_WEIGHT_SPECS: unknown = ${JSON.stringify(tiny.weights)};
/** Raw weight buffer for TinyFaceDetector, base64. */
export const TINY_FACE_DETECTOR_WEIGHTS_B64: string = ${JSON.stringify(tiny.b64)};

/** tfjs WeightsManifestEntry[] for FaceExpressionNet. */
export const FACE_EXPRESSION_WEIGHT_SPECS: unknown = ${JSON.stringify(expression.weights)};
/** Raw weight buffer for FaceExpressionNet, base64. */
export const FACE_EXPRESSION_WEIGHTS_B64: string = ${JSON.stringify(expression.b64)};

export const FACE_API_VERSION = ${JSON.stringify(version)};
`;

const outDir = path.join(__dirname, '..', 'src', 'generated');
fs.mkdirSync(outDir, { recursive: true });
const outFile = path.join(outDir, 'faceApiAssets.ts');
fs.writeFileSync(outFile, out);
console.log(`wrote ${outFile} (${(out.length / 1024 / 1024).toFixed(2)} MB, face-api ${version})`);
